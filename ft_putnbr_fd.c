#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;
	long	nbr;

	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		nbr = -(long)n;
	}
	else
		nbr = (long)n;
	if (nbr >= 10)
	{
		ft_putnbr_fd(nbr / 10, fd);
		ft_putnbr_fd(nbr % 10, fd);
	}
	else
	{
		c = nbr + '0';
		ft_putchar_fd(c, fd);
	}
	return ;
}
